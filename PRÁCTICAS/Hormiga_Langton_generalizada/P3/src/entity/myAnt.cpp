void myAnt::checkBorders(){
    int boardHeigth = board.getRows();
    int boardWidth = board.getCols();

	if((yPos == boardHeigth-1) || (xPos == boardWidth-1) || (yPos == 0) || (xPos == 0)){
		yPos = (((boardHeigth-1)/2)-1);
		xPos = (((boardWidth-1)/2)-1);
	}
	
	if ((boardHeigth-1)/2 == yPos){
	    //cout << "MOVIENDO HORMIGA EN DIRECCIÓN CONTRARIA: " << getDirection() << " NUEVA POS y: " << ypos << " NUEVA POS x: " << xpos " " <<endl;
	    if (getDirection() == '^'){
	        yPos = yPos -1;
	        setDirection(2);
	        cout << "MOVIENDO HORMIGA EN DIRECCIÓN CONTRARIA: " << getDirection() << " NUEVA POS y: " << yPos << " NUEVA POS x: " << xPos <<endl;
	    }
	   else if(getDirection() == 'v'){
	        yPos = yPos +1;
	        setDirection(2);
	        
	        cout << "MOVIENDO HORMIGA EN DIRECCIÓN CONTRARIA: " << getDirection() << " NUEVA POS y: " << yPos << " NUEVA POS x: " << xPos <<endl;
	   }
	}
}
